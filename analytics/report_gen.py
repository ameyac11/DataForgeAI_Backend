BRAND = "DataForgeAI"
WATERMARK_COLOR = colors.Color(0.85, 0.85, 0.92, alpha=0.35)
BRAND_PURPLE = "#7c3aed"
BRAND_INDIGO = "#4f46e5"
BRAND_VIOLET_LIGHT = "#ede9fe"
CHART_PALETTE = ["#7c3aed", "#4f46e5", "#06b6d4", "#10b981", "#f59e0b", "#ef4444", "#8b5cf6", "#ec4899"]

# Common chart dimensions
CHART_W = 5.8  # inches
CHART_H = 3.2  # inches
SMALL_CHART_W = 4.5
SMALL_CHART_H = 2.8


def _watermark(canvas, doc):
    """Draw diagonal watermark + footer on every page."""
    canvas.saveState()
    canvas.setFont("Helvetica-Bold", 52)
    canvas.setFillColor(WATERMARK_COLOR)
    canvas.translate(A4[0] / 2, A4[1] / 2)
    canvas.rotate(35)
    canvas.drawCentredString(0, 0, BRAND)
    canvas.restoreState()

    canvas.saveState()
    canvas.setFont("Helvetica", 7)
    canvas.setFillColor(colors.Color(0.5, 0.5, 0.5))
    canvas.drawString(30 * mm, 12 * mm, f"Generated by {BRAND}")
    canvas.drawRightString(A4[0] - 30 * mm, 12 * mm, f"Page {doc.page}")
    canvas.restoreState()


