def generate_pdf(df: pd.DataFrame, filename: str, file_size: int) -> bytes:
    buf = io.BytesIO()
    doc = SimpleDocTemplate(
        buf, pagesize=A4,
        topMargin=25 * mm, bottomMargin=25 * mm,
        leftMargin=20 * mm, rightMargin=20 * mm,
    )

    styles = getSampleStyleSheet()
    styles.add(ParagraphStyle("BrandTitle", parent=styles["Title"], fontSize=22, textColor=colors.HexColor(BRAND_PURPLE), spaceAfter=6))
    styles.add(ParagraphStyle("SectionHead", parent=styles["Heading2"], fontSize=14, textColor=colors.HexColor(BRAND_INDIGO), spaceBefore=16, spaceAfter=8))
    styles.add(ParagraphStyle("SubHead", parent=styles["Heading3"], fontSize=11, spaceBefore=10, spaceAfter=4))
    styles.add(ParagraphStyle("InfoMsg", parent=styles["Normal"], fontSize=9, textColor=colors.HexColor("#6b7280"), spaceAfter=6))
    styles.add(ParagraphStyle("CellText", parent=styles["Normal"], fontSize=8, leading=10))

    story: list = []

    # ── Title Header ────────────────────────────────────────────────
    story.append(Spacer(1, 50))
    story.append(Paragraph(f"{BRAND} Analytics Report", styles["BrandTitle"]))
    story.append(Spacer(1, 8))
    story.append(Paragraph(f"<b>File:</b> {filename}", styles["Normal"]))
    story.append(Paragraph(f"<b>Generated:</b> {datetime.now(timezone(timedelta(hours=5, minutes=30))).strftime('%B %d, %Y %H:%M IST')}", styles["Normal"]))
    story.append(Spacer(1, 6))
    story.append(HRFlowable(width="100%", color=colors.HexColor("#e5e7eb"), thickness=1))
    story.append(Spacer(1, 16))

    # ── 1. Dataset Summary ──────────────────────────────────────────
    summary = dataset_summary(df, filename, file_size)
    story.append(Paragraph("1. Dataset Summary", styles["SectionHead"]))
    summary_rows = [
        ["Metric", "Value"],
        ["Rows", f"{summary['rows']:,}"],
        ["Columns", str(summary["columns"])],
        ["Numeric Columns", str(summary["numeric_columns"])],
        ["Categorical Columns", str(summary["categorical_columns"])],
        ["Missing Values", f"{summary['missing_values']:,} ({summary['missing_pct']}%)"],
        ["Duplicates", f"{summary['duplicates']:,}"],
        ["Memory Usage", summary["memory_display"]],
        ["File Size", summary["file_size_display"]],
    ]
    story.append(_make_table(summary_rows))
    story.append(Spacer(1, 14))

    # ── 2. Column Analysis ──────────────────────────────────────────
    cols = column_analysis(df)
    story.append(Paragraph("2. Column Analysis", styles["SectionHead"]))
    col_rows = [["Column", "Type", "Category", "Missing", "Unique"]]
    for c in cols:
        col_rows.append([
            c["name"][:28], c["dtype"], c["category"],
            f"{c['missing']} ({c['missing_pct']}%)", str(c["unique"]),
        ])
    story.append(_make_table(col_rows))
    story.append(Spacer(1, 14))

    # ── 3. Numeric Statistics ───────────────────────────────────────
    numerics = [c for c in cols if c["category"] == "numeric"]
    categoricals = [c for c in cols if c["category"] == "categorical"]

    if numerics:
        story.append(Paragraph("3. Numeric Column Statistics", styles["SectionHead"]))
        num_rows = [["Column", "Mean", "Median", "Std", "Skew", "Min", "Max"]]
        for c in numerics:
            s = c.get("stats", {})
            num_rows.append([
                c["name"][:20],
                f"{s.get('mean', 0):.2f}", f"{s.get('median', 0):.2f}",
                f"{s.get('std', 0):.2f}", f"{s.get('skew', 0):.2f}",
                f"{s.get('min', 0):.2f}", f"{s.get('max', 0):.2f}",
            ])
        story.append(_make_table(num_rows))
        story.append(Spacer(1, 14))
    else:
        story.append(Paragraph("3. Numeric Column Statistics", styles["SectionHead"]))
        story.append(Paragraph("No numeric columns found in this dataset.", styles["InfoMsg"]))

    # ── 4. Distribution Charts ──────────────────────────────────────
    story.append(PageBreak())
    story.append(Paragraph("4. Distribution Charts", styles["SectionHead"]))

    if numerics:
        for c in numerics[:4]:
            try:
                series = df[c["name"]].dropna()
                if len(series) > 0:
                    story.append(Paragraph(f"<b>{c['name']}</b>", styles["SubHead"]))
                    story.append(_draw_histogram(df[c["name"]], c["name"]))
                    story.append(Spacer(1, 10))
            except Exception as e:
                logger.warning("Histogram for %s failed: %s", c["name"], e)
    else:
        story.append(Paragraph("No numeric columns available for distribution charts.", styles["InfoMsg"]))

    if categoricals:
        for c in categoricals[:3]:
            top_vals = c.get("top_values", [])[:8]
            if top_vals:
                labels = [v["value"] for v in top_vals]
                counts = [v["count"] for v in top_vals]
                try:
                    story.append(Paragraph(f"<b>{c['name']}</b>", styles["SubHead"]))
                    story.append(_draw_bar_chart(labels, counts, c["name"]))
                    story.append(Spacer(1, 10))
                except Exception as e:
                    logger.warning("Bar chart for %s failed: %s", c["name"], e)

    # pie chart for first categorical with ≤ 8 unique values
    pie_candidates = [c for c in categoricals if c["unique"] <= 8 and c.get("top_values")]
    if pie_candidates:
        c = pie_candidates[0]
        vals = c["top_values"][:8]
        try:
            story.append(Paragraph(f"<b>{c['name']}</b> — Pie Chart", styles["SubHead"]))
            story.append(_draw_pie([v["value"] for v in vals], [v["count"] for v in vals], c["name"]))
            story.append(Spacer(1, 10))
        except Exception as e:
            logger.warning("Pie chart for %s failed: %s", c["name"], e)

    # ── 5. Correlation Analysis ─────────────────────────────────────
    story.append(PageBreak())
    story.append(Paragraph("5. Correlation Analysis", styles["SectionHead"]))

    corr = correlation_matrix(df)
    if corr.get("message") and len(corr["columns"]) < 2:
        story.append(Paragraph(corr["message"], styles["InfoMsg"]))
    else:
        # heatmap image
        try:
            story.append(_draw_heatmap(corr))
            story.append(Spacer(1, 12))
        except Exception as e:
            logger.warning("Heatmap failed: %s", e)

        # top correlations table
        pairs = _top_correlations(corr, top_n=10)
        if pairs:
            story.append(Paragraph("Top Correlation Pairs", styles["SubHead"]))
            pair_rows = [["Column A", "Column B", "Correlation"]]
            for a, b, v in pairs:
                pair_rows.append([a, b, f"{v:.3f}"])
            story.append(_make_table(pair_rows))
            story.append(Spacer(1, 14))

    # ── 6. Scatter Plots ────────────────────────────────────────────
    num_col_names = [c["name"] for c in numerics]
    if len(num_col_names) >= 2:
        story.append(Paragraph("6. Scatter Plots", styles["SectionHead"]))
        # pick top correlated pairs for scatter plots
        scatter_pairs = _top_correlations(corr, top_n=3) if len(corr["columns"]) >= 2 else []
        if not scatter_pairs and len(num_col_names) >= 2:
            scatter_pairs = [(num_col_names[0], num_col_names[1], 0)]
        for col_a, col_b, _ in scatter_pairs[:3]:
            try:
                story.append(Paragraph(f"<b>{col_a}</b> vs <b>{col_b}</b>", styles["SubHead"]))
                story.append(_draw_scatter(df, col_a, col_b))
                story.append(Spacer(1, 10))
            except Exception as e:
                logger.warning("Scatter for %s vs %s failed: %s", col_a, col_b, e)
    else:
        story.append(Paragraph("6. Scatter Plots", styles["SectionHead"]))
        story.append(Paragraph("Scatter plots require at least 2 numeric columns. This dataset does not meet that requirement.", styles["InfoMsg"]))

    # ── 7. Box Plots ────────────────────────────────────────────────
    story.append(PageBreak())
    story.append(Paragraph("7. Box Plots", styles["SectionHead"]))
    if numerics:
        bp_cols = num_col_names[:6]
        try:
            story.append(_draw_boxplot(df, bp_cols))
            story.append(Spacer(1, 14))
        except Exception as e:
            logger.warning("Boxplot failed: %s", e)
    else:
        story.append(Paragraph("No numeric columns available for box plot analysis.", styles["InfoMsg"]))

    # ── 8. Outlier Detection ────────────────────────────────────────
    story.append(Paragraph("8. Outlier Detection (IQR Method)", styles["SectionHead"]))
    if numerics:
        out_rows = [["Column", "Outliers", "% of Data", "Lower Fence", "Upper Fence"]]
        for c in numerics[:10]:
            od = outlier_detection(df, c["name"])
            if od.get("error"):
                continue
            out_rows.append([
                c["name"][:20], str(od.get("outlier_count", 0)),
                f"{od.get('outlier_pct', 0)}%",
                f"{od.get('lower_fence', 0):.2f}", f"{od.get('upper_fence', 0):.2f}",
            ])
        if len(out_rows) > 1:
            story.append(_make_table(out_rows))
        else:
            story.append(Paragraph("No outlier data could be computed.", styles["InfoMsg"]))
    else:
        story.append(Paragraph("No numeric columns available for outlier detection.", styles["InfoMsg"]))
    story.append(Spacer(1, 14))

    # ── 9. Categorical Insights ─────────────────────────────────────
    if categoricals:
        story.append(Paragraph("9. Categorical Columns — Top Values", styles["SectionHead"]))
        for c in categoricals[:6]:
            story.append(Paragraph(f"<b>{c['name']}</b> ({c['unique']} unique values)", styles["SubHead"]))
            tv = c.get("top_values", [])[:5]
            cat_rows = [["Value", "Count"]]
            for item in tv:
                cat_rows.append([item["value"][:35], str(item["count"])])
            story.append(_make_table(cat_rows, col_widths=[3.5 * inch, 1.5 * inch]))
            story.append(Spacer(1, 6))

    doc.build(story, onFirstPage=_watermark, onLaterPages=_watermark)
    return buf.getvalue()


