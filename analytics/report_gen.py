# Chart rendering helpers (matplotlib → PNG bytes → ReportLab Image)
# ---------------------------------------------------------------------------

def _fig_to_image(fig, width=CHART_W, height=CHART_H) -> Image:
    """Convert a matplotlib figure to a ReportLab Image flowable."""
    buf = io.BytesIO()
    fig.savefig(buf, format="png", dpi=150, bbox_inches="tight", facecolor="white", edgecolor="none")
    plt.close(fig)
    buf.seek(0)
    return Image(buf, width=width * inch, height=height * inch)


def _style_ax(ax, title="", xlabel="", ylabel=""):
    """Apply consistent styling to axes."""
    ax.set_title(title, fontsize=11, fontweight="bold", color="#1f2937", pad=10)
    ax.set_xlabel(xlabel, fontsize=9, color="#4b5563")
    ax.set_ylabel(ylabel, fontsize=9, color="#4b5563")
    ax.tick_params(labelsize=8, colors="#6b7280")
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.spines["left"].set_color("#d1d5db")
    ax.spines["bottom"].set_color("#d1d5db")
    ax.grid(axis="y", alpha=0.3, color="#9ca3af")


def _draw_histogram(series: pd.Series, col_name: str, bins: int = 25) -> Image:
    fig, ax = plt.subplots(figsize=(CHART_W, CHART_H))
    ax.hist(series.dropna(), bins=bins, color=BRAND_PURPLE, alpha=0.8, edgecolor="white", linewidth=0.5)
    _style_ax(ax, title=f"Distribution — {col_name}", xlabel=col_name, ylabel="Frequency")
    fig.tight_layout()
    return _fig_to_image(fig)


def _draw_bar_chart(labels: list, counts: list, col_name: str) -> Image:
    fig, ax = plt.subplots(figsize=(CHART_W, CHART_H))
    short_labels = [str(l)[:18] for l in labels]
    bars = ax.barh(short_labels[::-1], counts[::-1], color=CHART_PALETTE[:len(labels)], edgecolor="white", linewidth=0.5)
    _style_ax(ax, title=f"Top Values — {col_name}", xlabel="Count", ylabel="")
    fig.tight_layout()
    return _fig_to_image(fig)


def _draw_scatter(df: pd.DataFrame, col_x: str, col_y: str) -> Image:
    fig, ax = plt.subplots(figsize=(CHART_W, CHART_H))
    sample = df[[col_x, col_y]].dropna()
    if len(sample) > 500:
        sample = sample.sample(500, random_state=42)
    ax.scatter(sample[col_x], sample[col_y], alpha=0.5, s=18, color=BRAND_PURPLE, edgecolors="white", linewidth=0.3)
    _style_ax(ax, title=f"Scatter — {col_x} vs {col_y}", xlabel=col_x, ylabel=col_y)
    fig.tight_layout()
    return _fig_to_image(fig)


def _draw_heatmap(corr_dict: dict) -> Image:
    cols = corr_dict["columns"]
    matrix = np.array(corr_dict["matrix"])
    fig, ax = plt.subplots(figsize=(min(CHART_W + 1, 7), min(CHART_H + 1, 5)))
    cmap = sns.diverging_palette(260, 20, as_cmap=True)
    sns.heatmap(
        matrix, annot=True, fmt=".2f", cmap=cmap,
        xticklabels=cols, yticklabels=cols,
        center=0, vmin=-1, vmax=1, linewidths=0.5, linecolor="white",
        ax=ax, cbar_kws={"shrink": 0.8},
        annot_kws={"size": 7},
    )
    ax.set_title("Correlation Heatmap", fontsize=12, fontweight="bold", color="#1f2937", pad=12)
    ax.tick_params(labelsize=7)
    plt.setp(ax.get_xticklabels(), rotation=45, ha="right")
    fig.tight_layout()
    return _fig_to_image(fig, width=min(CHART_W + 1, 7), height=min(CHART_H + 1, 5))


def _draw_boxplot(df: pd.DataFrame, columns: list[str]) -> Image:
    fig, ax = plt.subplots(figsize=(CHART_W, CHART_H))
    data_to_plot = [df[c].dropna().values for c in columns]
    bp = ax.boxplot(
        data_to_plot, labels=[c[:15] for c in columns], patch_artist=True,
        boxprops=dict(facecolor=BRAND_VIOLET_LIGHT, edgecolor=BRAND_PURPLE, linewidth=1),
        medianprops=dict(color="#dc2626", linewidth=1.5),
        whiskerprops=dict(color=BRAND_PURPLE, linewidth=1),
        capprops=dict(color=BRAND_PURPLE, linewidth=1),
        flierprops=dict(marker="o", markersize=3, markerfacecolor="#f59e0b", markeredgecolor="#f59e0b", alpha=0.6),
    )
    _style_ax(ax, title="Box Plot — Numeric Columns", xlabel="", ylabel="Value")
    plt.setp(ax.get_xticklabels(), rotation=30, ha="right")
    fig.tight_layout()
    return _fig_to_image(fig)


def _draw_pie(labels: list, values: list, col_name: str) -> Image:
    fig, ax = plt.subplots(figsize=(SMALL_CHART_W, SMALL_CHART_H))
    clrs = CHART_PALETTE[:len(labels)]
    wedges, texts, autotexts = ax.pie(
        values, labels=None, autopct="%1.1f%%", startangle=90, colors=clrs,
        pctdistance=0.8, textprops={"fontsize": 7},
    )
    ax.legend(
        wedges, [str(l)[:20] for l in labels],
        loc="center left", bbox_to_anchor=(1.0, 0.5), fontsize=7, frameon=False,
    )
    ax.set_title(f"Distribution — {col_name}", fontsize=10, fontweight="bold", color="#1f2937")
    fig.tight_layout()
    return _fig_to_image(fig, width=SMALL_CHART_W, height=SMALL_CHART_H)


